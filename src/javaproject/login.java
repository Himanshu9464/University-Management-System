    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
  String mypath = "jdbc:mysql://localhost/javaproject";
        String myuser = "root";
        String mypassword = "";
        try 
    {
         Connection conn = DriverManager.getConnection(mypath, myuser, mypassword); 
         try
         {
            String qry = "select * from managefaculty where username=? and password=?"; 
             
            PreparedStatement st = conn.prepareStatement(qry);
            st.setString(1, jTextField1.getText()); 
            st.setString(2, jPasswordField1.getText()); 
            ResultSet rst = st.executeQuery();  
            
            
            if(rst.next())
            {
                String ut = rst.getString("usertype");  

                this.dispose();
                homepage.utype=ut;
                homepage obj = new homepage();
                obj.setVisible(true);
                
            }
            else 
               JOptionPane.showMessageDialog(rootPane,"Wrong username or password");  
         }
        catch (Exception ex) 
        {
             JOptionPane.showMessageDialog(rootPane,"Query Error : "+ex.getMessage());
        }  
    } 
    catch (Exception ex) 
    {
         JOptionPane.showMessageDialog(rootPane,"Database Error : "+ex.getMessage());
    }          
    }//GEN-LAST:event_jButton1ActionPerformed

